public class App {
    public static void main(String[] args) throws Exception {
        // OPERATOR ASSIGNMENT

        // penjumlahan
        int a = 10;
        a += 12;
        System.out.println("a = " + a);

        // pengurangan
        int b = 10;
        b -= 12;
        System.out.println("b = " + b);

        // perkalian
        int c = 10;
        c *= 12;
        System.out.println("c = " + c);

        // pembagian
        int d = 10;
        d /= 12;
        System.out.println("d = " + d);

        // modulus
        int e = 10;
        e %= 12;
        System.out.println("e = " + e);

        System.out.println("mantapüëç");
    }
}
